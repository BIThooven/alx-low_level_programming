#include "3-calc.h"
/**
 * op_add - a function that calculates the sum
 * @a: a value to be added
 * @b: a value to be added
 * Return: returns the sum of the 2 values
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a function that calculates the sub
 * @a: a value to be subtracted
 * @b: a value to subtract
 * Return: returns subtraction of 2 values
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a function that calculates the mul
 * @a: a value to be multiplied
 * @b: a value to be multiplied
 * Return: returns the multiplication of 2 values
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a function that calculates the division
 * @a: a value to be divised
 * @b: the divisor value
 * Return: returns the divison of 2 values
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a function that calculates the modulo
 * @a: a value to be moduled
 * @b: a value to be moduled
 * Return: returns the modulo of 2 values
*/
int op_mod(int a, int b)
{
	return (a % b);
}
